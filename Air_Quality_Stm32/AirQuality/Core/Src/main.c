//Get ADC value using adc 1
uint16_t ADC_GetValue(uint32_t channel){
		ADC_ChannelConfTypeDef conf = {
				.Channel = channel,
				.Rank = 1,
				.SamplingTime = ADC_SAMPLETIME_1CYCLE_5,
		};
		if (HAL_ADC_ConfigChannel(&hadc1, &conf) != HAL_OK) {
        Error_Handler();
    }
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1,HAL_MAX_DELAY);
		uint16_t res = HAL_ADC_GetValue(&hadc1);
		HAL_ADC_Stop(&hadc1);
		return res;
}

//Update new threshold and fan speed by using UART
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){
		if(huart->Instance == huart1.Instance){
				//Get string
				HAL_UART_Receive(huart,rxBuffer,sizeof(rxBuffer),HAL_MAX_DELAY);
				
				//Get data from string
				sscanf((char*)rxBuffer,"%d %d %d\n\r",
						(int*)&fanPWM,(int*)&coThreshold,(int*)&dustThreshold);
		}
}

